package conditons;

public class Ifelseifladder {

	public static void main(String[] args) {
		int i =10;
		if(i==10) {
			System.out.println("i is 10");
		}else if(i==15) {
			System.out.println("i is 15");
		}else if(i==20) {
			System.out.println("i is 20");
		}else {
			System.out.println("i not present");
		}
		
	}

}
