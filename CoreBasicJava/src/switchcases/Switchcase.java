package switchcases;

public class Switchcase {

	public static void main(String[] args) {
		int value=3;
		switch(value) {
		case 1:
		System.out.println("here is the case 1");
		break;
		case 2:
			System.out.println("here is the case 2");
			break;
		case 3:
			System.out.println("here is the case 3");
			break;
		default:
			System.out.println("no switch case match");
			break;
			
		}
	}

}
