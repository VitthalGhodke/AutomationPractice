package practice;

public class ForloopForMultipletext {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			System.out.println("Hello");
		}
		System.out.println("********************");
     for(char i='A';i<='Z';i++) {
    	 System.out.print(i);
     }
     System.out.println();
     System.out.println("**************");
     for(char i='a'; i<='z';i++) {
    	 System.out.print(i);
     }
     for(char i='z'; i>='a';i--) {
    	 System.out.println(i);
     }
	}

}
