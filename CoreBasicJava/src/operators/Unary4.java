package operators;

public class Unary4 {

	public static void main(String[] args) {
		int r=6;
		System.out.println(r++);
		System.out.println(r);
	
		int s=6;
		System.out.println(++s);
		System.out.println(s);
		int x=6;
		System.out.println(x--);
		System.out.println(x);
		
		int y=6;
		System.out.println(--y);
		System.out.println(y);
		
		

	}

}
